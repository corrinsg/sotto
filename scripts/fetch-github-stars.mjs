#!/usr/bin/env node
// Fetches the current star count for the repo and writes it to
// lib/generated/github-stars.ts, so the StarButton can display a live
// number without making a runtime GitHub API call (which would mean
// loosening our CSP and adding a third-party dependency to every page
// render).
//
// Runs as `prebuild`, so every Vercel production deploy picks up the
// latest count. If the fetch fails (offline, rate limited, transient
// GitHub outage) we log a warning and leave the previously-committed
// value in place rather than failing the build.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";

const REPO = "corrinsg/sotto";
const OUT = "lib/generated/github-stars.ts";

function write(count) {
  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(
    OUT,
    `// Auto-generated by scripts/fetch-github-stars.mjs during prebuild.\n// Do not edit by hand — the value is overwritten on every \`npm run build\`.\nexport const GITHUB_STAR_COUNT = ${count};\n`,
  );
}

function readCurrent() {
  try {
    const src = readFileSync(OUT, "utf8");
    const m = /GITHUB_STAR_COUNT\s*=\s*(\d+)/.exec(src);
    return m ? Number(m[1]) : 0;
  } catch {
    return 0;
  }
}

try {
  const res = await fetch(`https://api.github.com/repos/${REPO}`, {
    headers: {
      "User-Agent": "sotto-star-fetcher",
      Accept: "application/vnd.github+json",
    },
  });
  if (!res.ok) throw new Error(`GitHub API ${res.status}`);
  const data = await res.json();
  const count = Number(data.stargazers_count ?? 0);
  write(count);
  console.log(`[stars] ${REPO}: ${count}`);
} catch (err) {
  const prev = readCurrent();
  console.warn(
    `[stars] fetch failed (${err instanceof Error ? err.message : err}); keeping previous value ${prev}`,
  );
  write(prev);
}
